a = true
b = false

if (a and b)
then
    print("true and false is true")
else
    print("true and false is false")
end


if (not(a and b))
then
    print("not(true and false) is true")
else
    print("not(true and false) is false")
end

if (a or b)
then
    print("true or false is true")
else
    print("true or false is false")
end


if (not(a or b))
then
    print("not(true or false) is true")
else
    print("not(true or false) is false")
end
